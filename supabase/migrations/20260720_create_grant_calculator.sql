create policy "Authenticated users can read grant settings"
on public.grant_settings for select
to authenticated
using (true);

create policy "Admins can manage grant settings"
on public.grant_settings for all
to authenticated
using (
  exists (
    select 1 from public.profiles
    where profiles.id = auth.uid()
      and profiles.role in ('owner', 'admin')
  )
)
with check (
  exists (
    select 1 from public.profiles
    where profiles.id = auth.uid()
      and profiles.role in ('owner', 'admin')
  )
);

