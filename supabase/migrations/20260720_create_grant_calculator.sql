create table if not exists public.grant_settings (
  id smallint primary key default 1 check (id = 1),
  program_name text not null default 'Grant OZE — Radzionków',
  fixed_margin_gross numeric(12, 2) not null default 3000 check (fixed_margin_gross >= 0),
  min_pv_kw numeric(6, 3) not null default 2 check (min_pv_kw > 0),
  max_pv_kw numeric(6, 3) not null default 10 check (max_pv_kw >= min_pv_kw),
  annual_yield_kwh_per_kwp numeric(8, 2) not null default 1000 check (annual_yield_kwh_per_kwp > 0),
  heat_pump_extra_pv_kw numeric(6, 3) not null default 4 check (heat_pump_extra_pv_kw >= 0),
  pv_eligible_limit_gross numeric(12, 2) not null default 20000 check (pv_eligible_limit_gross >= 0),
  extra_pv_eligible_limit_gross numeric(12, 2) not null default 20000 check (extra_pv_eligible_limit_gross >= 0),
  pv_limit_gross_per_kwp numeric(12, 2) not null default 5000 check (pv_limit_gross_per_kwp >= 0),
  heat_pump_eligible_limit_gross numeric(12, 2) not null default 35000 check (heat_pump_eligible_limit_gross >= 0),
  combined_grant_limit_gross numeric(12, 2) not null default 55000 check (combined_grant_limit_gross >= 0),
  vat_rate numeric(5, 2) not null default 23 check (vat_rate >= 0),
  active boolean not null default true,
  updated_at timestamptz not null default now(),
  updated_by uuid references auth.users(id)
);

