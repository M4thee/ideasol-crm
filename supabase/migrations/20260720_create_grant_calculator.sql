create table if not exists public.grant_panels (
  id bigint generated by default as identity primary key,
  manufacturer text not null,
  model text not null,
  power_wp integer not null check (power_wp > 0),
  price_gross numeric(12, 2) not null default 0 check (price_gross >= 0),
  catalog_card_url text,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.grant_inverters (
  id bigint generated by default as identity primary key,
  manufacturer text not null,
  model text not null,
  power_kw numeric(6, 3) not null check (power_kw > 0),
  phase_count smallint not null default 3 check (phase_count in (1, 3)),
  price_gross numeric(12, 2) not null default 0 check (price_gross >= 0),
  catalog_card_url text,
  program_compliant boolean not null default true,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.grant_heat_pumps (
  id bigint generated by default as identity primary key,
  manufacturer text not null,
  model text not null,
  power_kw numeric(6, 3) not null check (power_kw > 0),
  pump_type text not null default 'monoblock' check (pump_type in ('monoblock', 'split')),
  price_gross numeric(12, 2) not null default 0 check (price_gross >= 0),
  cop numeric(5, 2) check (cop is null or cop > 0),
  zum_compliant boolean not null default false,
  zum_url text,
  catalog_card_url text,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

